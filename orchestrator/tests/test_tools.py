class FakeResponse:
    def __init__(self, payload: dict):
        self.payload = payload

    def __enter__(self):
        return self

    def __exit__(self, exc_type, exc, traceback):
        return False

    def read(self) -> bytes:
        return json.dumps(self.payload).encode("utf-8")


class FakeTextResponse:
    def __init__(self, text: str):
        self.text = text

    def __enter__(self):
        return self

    def __exit__(self, exc_type, exc, traceback):
        return False

    def read(self) -> bytes:
        return self.text.encode("utf-8")


def test_file_write_and_read_are_scoped_to_tool_workspace(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("AGENTFLOW_TOOL_WORKDIR", str(tmp_path))
    monkeypatch.setenv("ORCHESTRATOR_PUBLIC_BASE_URL", "http://localhost:8000")
    content = json.dumps(
        {
            "file_write": {"path": "notes/hello.txt", "content": "hello from tool"},
            "file_read": {"path": "notes/hello.txt"},
        }
    )

    calls = tools.run_allowed_tools({"file_write", "file_read"}, content, {})

    assert [call["toolName"] for call in calls] == ["file_write", "file_read"]
    assert calls[0]["status"] == "COMPLETED"
    assert calls[0]["result"]["bytesWritten"] == len("hello from tool")
    assert calls[0]["result"]["publicUrl"] == "http://localhost:8000/tool-files/notes/hello.txt"
    assert calls[1]["status"] == "COMPLETED"
    assert calls[1]["result"]["content"] == "hello from tool"
    assert (tmp_path / "notes" / "hello.txt").read_text(encoding="utf-8") == "hello from tool"


