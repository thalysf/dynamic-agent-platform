def test_web_search_tries_simple_fallback_queries(monkeypatch: pytest.MonkeyPatch) -> None:
    seen_urls: list[str] = []

    def fake_urlopen(request, timeout):
        seen_urls.append(request.full_url)
        if "q=GitHub+issue+triage" in request.full_url and "html.duckduckgo.com" in request.full_url:
            return FakeTextResponse(
                """
                <a class="result__a" href="https://example.test/github-issue-triage">GitHub issue triage guide</a>
                <a class="result__snippet">A practical guide to triaging GitHub issues.</a>
                """
            )
        if "api.duckduckgo.com" in request.full_url:
            return FakeTextResponse("")
        if "html.duckduckgo.com" in request.full_url:
            return FakeTextResponse("<html></html>")
        if "wikipedia.org" in request.full_url:
            return FakeResponse(["", [], [], []])
        raise AssertionError(f"Unexpected URL: {request.full_url}")

    monkeypatch.setattr(tools.urllib.request, "urlopen", fake_urlopen)

    calls = tools.run_allowed_tools({"web_search"}, "very specific query with no direct results", {})

    assert calls[0]["status"] == "COMPLETED"
    assert calls[0]["result"]["query"] == "GitHub issue triage"
    assert calls[0]["result"]["originalQuery"] == "very specific query with no direct results"
    assert calls[0]["result"]["results"][0]["title"] == "GitHub issue triage guide"
    assert any("very+specific+query" in url for url in seen_urls)


